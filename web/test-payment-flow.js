import dotenv from "dotenv";
import path from "path";
import { fileURLToPath } from "url";

// Emulate __dirname in ES modules
const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// 1. Load variables from .env.local
dotenv.config({ path: path.resolve(__dirname, ".env.local") });

// ANSI escape codes for coloring terminal output
const colors = {
  reset: "\x1b[0m",
  green: "\x1b[32m",
  yellow: "\x1b[33m",
  blue: "\x1b[34m",
  cyan: "\x1b[36m",
  red: "\x1b[31m",
  bold: "\x1b[1m",
};

async function runTest() {
  console.log(`${colors.bold}${colors.cyan}======================================================${colors.reset}`);
  console.log(`${colors.bold}${colors.cyan}    🚀 STARTING MOCK RAZORPAY VERIFICATION TEST       ${colors.reset}`);
  console.log(`${colors.bold}${colors.cyan}======================================================${colors.reset}\n`);

  // Target API Route - Make sure your dev server is running on port 3000
  // Note: Targeting /api/razorpay/verify-payment based on your file structure
  const API_URL = "http://localhost:3000/api/razorpay/verify-payment";

  const randomNum = Date.now().toString().slice(-4);
  
  // 2. Mock Razorpay checkout payload
  const payload = {
    razorpay_order_id: `order_test_${randomNum}`,
    razorpay_payment_id: `pay_test_${randomNum}`,
    razorpay_signature: "mock_signature_string",
    // Replace this with an actual valid course UUID from your phase2.courses table!
    course_id: "3da53c8c-cf0d-428b-96d2-8665d405c106",
    bundle_id: "350edd44-752b-454a-8bf3-0ffb1915fd78",
  };

  const mockEmail = `tester${randomNum}@example.com`;
  const mockName = `Test User ${randomNum}`;

  console.log(`${colors.yellow}► Preparing to send mock payload:${colors.reset}`);
  console.log(JSON.stringify(payload, null, 2));
  console.log(`${colors.yellow}► Targeting URL: ${colors.reset}${API_URL}\n`);

  try {
    // 3. Send POST request using native fetch (Available in Node 18+)
    const response = await fetch(API_URL, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        // Passing custom headers so our development bypass can read the mock customer details
        "x-mock-email": mockEmail,
        "x-mock-name": mockName,
      },
      body: JSON.stringify(payload),
    });

    const data = await response.json();

    // 4. Detailed console logging based on response
    console.log(`${colors.bold}--- RESPONSE RECEIVED ---${colors.reset}`);

    if (response.ok) {
      console.log(`${colors.green}✔ Status Code : ${response.status} ${response.statusText}${colors.reset}`);
      console.log(`${colors.green}✔ Success     : ${data.success}${colors.reset}`);
      console.log(`${colors.green}✔ Message     : ${data.message}${colors.reset}`);

      console.log(`\n${colors.blue}${colors.bold}➡ Redirection URL generated by Backend:${colors.reset}`);
      console.log(`${colors.cyan}${data.redirectUrl}${colors.reset}\n`);
    } else {
      console.log(`${colors.red}✖ Status Code : ${response.status} ${response.statusText}${colors.reset}`);
      console.log(`${colors.red}✖ Error       : ${data.error}${colors.reset}\n`);
    }

  } catch (error) {
    console.error(`${colors.red}${colors.bold}✖ Network or execution error occurred:${colors.reset}`, error);
    console.log(`${colors.yellow}Tip: Make sure your Next.js development server is running (npm run dev)!${colors.reset}`);
  }
}

runTest();
